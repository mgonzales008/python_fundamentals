'''

Write the necessary code to display the area and perimeter of a rectangle that has a width of 2.4 and a height of 6.4.

'''

w = 2.4
l = 6.4

rectangle_area = w * l
rectangle_perimeter = 2 * (w + l)

print('the area of a rectangle that has a width of 2.4 and a height of 6.4. is ' + str(rectangle_area))
print('the perimeter of a rectangle that has a width of 2.4 and a height of 6.4. is ' + str(rectangle_perimeter))